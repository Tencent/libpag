static constexpr const char* GENERATED_COMMENT =
    "<!-- Generated by PAGX HTMLExporter. Do not edit. -->\n";

static std::string WrapAsHTMLDocument(const std::string& fragment, float width, float height) {
  auto w = CssFloatToString(width);
  auto h = CssFloatToString(height);
  std::string bodyStyle = "margin:0;padding:0;width:" + w + "px;height:" + h +
                          "px;overflow:hidden;font-family:sans-serif";
  return "<!DOCTYPE html>\n<html>\n<head>\n<meta charset=\"utf-8\">\n<style>\nbody { " + bodyStyle +
         " }\n</style>\n</head>\n<body>\n" + fragment + "\n</body>\n</html>\n";
}

std::string HTMLExporter::ToHTML(PAGXDocument& doc, const std::string& resourceDir,
                                 HTMLOutputMode mode, const Options& options,
                                 std::string* errorMsg) {
  if (resourceDir.empty()) {
    if (errorMsg) {
      *errorMsg = "resourceDir must not be empty.";
    }
    std::cerr << "HTMLExporter::ToHTML: resourceDir must not be empty. "
                 "Use HTMLExporter::ToFile for automatic resource-directory derivation."
              << std::endl;
    return {};
  }
  if (!std::filesystem::path(resourceDir).is_absolute()) {
    if (errorMsg) {
      *errorMsg = "resourceDir must be an absolute path, got '" + resourceDir + "'.";
    }
    std::cerr << "HTMLExporter::ToHTML: resourceDir must be an absolute path, got '" << resourceDir
              << "'." << std::endl;
    return {};
  }
  // Resolve symlinks to prevent path-traversal attacks via symlinked directories.
  std::error_code canonEc;
  auto canonicalDir = std::filesystem::weakly_canonical(resourceDir, canonEc);
  if (canonEc) {
    if (errorMsg) {
      *errorMsg = "resourceDir path cannot be resolved: " + canonEc.message();
    }
    return {};
  }
  if (!doc.isLayoutApplied()) {
    doc.applyLayout();
  }
  // Derive the URL prefix used by <img src=...> from the resource directory's basename. This
  // assumes the caller will write the returned HTML string to a file located in resourceDir's
  // parent directory (ToFile enforces this layout automatically).
  auto resourceDirPath = std::filesystem::path(resourceDir);
  std::string urlPrefix = resourceDirPath.filename().string();
  if (!urlPrefix.empty()) {
    urlPrefix += '/';
  }

  HTMLBuilder html(0, 4096);
  HTMLBuilder defs(2, 4096);
  HTMLWriterContext ctx;
  ctx.docWidth = doc.width;
  ctx.docHeight = doc.height;
  ctx.staticImgDir = resourceDir;
  ctx.staticImgUrlPrefix = urlPrefix;
  ctx.rasterScale = std::clamp(options.rasterScale, 0.01f, 4.0f);

  // Pre-pass: for every compatible PlusDarker Layer, render a cropped backdrop PNG with the layer
  // temporarily hidden. The resulting base64 data URLs are consumed by writeLayer below to emit an
  // SVG filter (feImage + feComposite arithmetic) that matches tgfx PlusDarker pixel-for-pixel.
  HTMLPlusDarkerRenderer::RenderAll(doc, resourceDir, urlPrefix, options.rasterScale,
                                    ctx.plusDarkerBackdrops);

  // Pre-pass: build WOFF2 fonts for embedded vector fonts. Each Font resource that contains
  // only vector glyphs (Glyph.path, no Glyph.image) is converted to a minimal OpenType-CFF
  // font compressed as WOFF2. The resulting files are written to {resourceDir}/fonts/ and
  // registered in ctx.woff2Fonts so writeEmbeddedShapeGlyphs can emit <span> with @font-face
  // instead of SVG <path> elements.
  std::string fontFaceRules;
  for (auto& nodePtr : doc.nodes) {
    if (nodePtr->nodeType() != NodeType::Font) {
      continue;
    }
    auto* font = static_cast<Font*>(nodePtr.get());
    if (font->glyphs.empty() || !font->glyphs[0]) {
      continue;
    }
    // Both vector (path) and bitmap (image) fonts are supported.
    if (!font->glyphs[0]->path && !font->glyphs[0]->image) {
      continue;
    }
    std::string fontId = ctx.nextId("f");
    auto result = BuildWoff2FromFont(font, fontId);
    if (result.woff2Data.empty()) {
      continue;
    }
    std::string filename = "font_" + fontId + ".woff2";
    result.relativeUrl = urlPrefix + "fonts/" + filename;
    std::string fontsDir = resourceDir + "/fonts";
    std::error_code ec;
    std::filesystem::create_directories(fontsDir, ec);
    std::ofstream f(fontsDir + "/" + filename, std::ios::binary);
    if (f.is_open()) {
      f.write(reinterpret_cast<const char*>(result.woff2Data.data()),
              static_cast<std::streamsize>(result.woff2Data.size()));
      f.flush();
      if (!f) {
        std::cerr << "HTMLExporter: failed to write font file: " << fontsDir << "/" << filename
                  << std::endl;
      }
    }
    fontFaceRules += "@font-face{font-family:'" + EscapeCssFontFamily(result.familyName) +
                     "';src:url('" + result.relativeUrl + "') format('woff2')}\n";
    ctx.woff2Fonts[font] = std::move(result);
  }

  HTMLWriter writer(&defs, &ctx);

  // Root div
  std::string rootStyle = "position:relative;width:" + CssFloatToString(doc.width) +
                          "px;height:" + CssFloatToString(doc.height) + "px;overflow:hidden";
  html.openTag("div");
  html.addAttr("data-pagx-version", "1.0");
  html.addAttr("style", rootStyle);
  html.closeTagStart();

  // Render layers into body content
  HTMLBuilder body(1, 4096);
  for (auto* layer : doc.layers) {
    writer.writeLayer(body, layer);
  }

  // Insert @font-face rules for embedded WOFF2 fonts
  if (!fontFaceRules.empty()) {
    html.openTag("style");
    html.closeTagStart();
    html.addRawContent(fontFaceRules);
    html.closeTag();  // </style>
  }

  // Insert SVG defs if any
  std::string defsStr = defs.release();
  if (!defsStr.empty()) {
    html.openTag("svg");
    html.addAttr("style", "position:absolute;width:0;height:0;overflow:hidden");
    html.closeTagStart();
    html.openTag("defs");
    html.closeTagStart();
    html.addRawContent(defsStr);
    html.closeTag();  // </defs>
    html.closeTag();  // </svg>
  }

  html.addRawContent(body.release());

  html.closeTag();  // </div>

  std::string nativeHTML = RoundCoordinatesInHTML(html.release());

  if (options.extractStyleSheet) {
    nativeHTML = HTMLStyleExtractor::Extract(nativeHTML);
  }

  std::string result = std::string(GENERATED_COMMENT) + nativeHTML;

  if (mode == HTMLOutputMode::FullDocument) {
    result = WrapAsHTMLDocument(result, doc.width, doc.height);
  }

  return result;
}

bool HTMLExporter::ToFile(PAGXDocument& document, const std::string& filePath,
                          const Options& options, std::string* errorMsg) {
  // Derive the resource directory as a sibling of the HTML file named after its stem:
  // "/tmp/abc.html" → resourceDir = "/tmp/abc".
  auto htmlPath = std::filesystem::absolute(std::filesystem::path(filePath));
  auto stem = htmlPath.stem().string();
  if (stem.empty()) {
    if (errorMsg) {
      *errorMsg = "filePath must have a non-empty stem: " + filePath;
    }
    std::cerr << "HTMLExporter::ToFile: filePath must have a non-empty stem: " << filePath
              << std::endl;
    return false;
  }
  auto parentDir = htmlPath.parent_path();
  auto resourceDir = parentDir / stem;

  // ToFile always produces a full HTML document (standalone file needs DOCTYPE).
  auto html =
      ToHTML(document, resourceDir.string(), HTMLOutputMode::FullDocument, options, errorMsg);
  if (html.empty()) {
    if (errorMsg && errorMsg->empty()) {
      *errorMsg = "document produced no HTML output.";
    }
    return false;
  }
  if (!parentDir.empty() && !std::filesystem::exists(parentDir)) {
    std::error_code ec = {};
    std::filesystem::create_directories(parentDir, ec);
    if (ec) {
      if (errorMsg) {
        *errorMsg = "failed to create directory: " + parentDir.string();
      }
      return false;
    }
  }
  std::ofstream file(filePath, std::ios::binary);
  if (!file) {
    if (errorMsg) {
      *errorMsg = "failed to write file: " + filePath;
    }
    return false;
  }
  file.write(html.data(), static_cast<std::streamsize>(html.size()));
  if (!file.good()) {
    if (errorMsg) {
      *errorMsg = "write error after opening file: " + filePath;
    }
    return false;
  }
  return true;
}

