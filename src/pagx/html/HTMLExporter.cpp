// Decode HTML entities back to raw characters before style parsing.
// This is necessary because escapeAttr() encodes quotes (&#39; &quot;) which
// contain semicolons that would be misinterpreted as CSS property separators.
static std::string DecodeHTMLEntities(const std::string& str) {
  std::string result;
  result.reserve(str.size());
  size_t i = 0;
  while (i < str.size()) {
    if (str[i] == '&') {
      if (str.compare(i, 5, "&#39;") == 0) {
        result += '\'';
        i += 5;
      } else if (str.compare(i, 6, "&quot;") == 0) {
        result += '"';
        i += 6;
      } else if (str.compare(i, 5, "&amp;") == 0) {
        result += '&';
        i += 5;
      } else if (str.compare(i, 4, "&lt;") == 0) {
        result += '<';
        i += 4;
      } else if (str.compare(i, 4, "&gt;") == 0) {
        result += '>';
        i += 4;
      } else {
        result += str[i++];
      }
    } else {
      result += str[i++];
    }
  }
  return result;
}

static std::string TransformStyleAttributes(const std::string& html, bool useDoubleQuotes) {
  std::string result;
  result.reserve(html.size() * 2);
  size_t pos = 0;

  while (pos < html.size()) {
    size_t stylePos = html.find("style=\"", pos);
    if (stylePos == std::string::npos) {
      result += html.substr(pos);
      break;
    }

    result += html.substr(pos, stylePos - pos);

    size_t valueStart = stylePos + 7;
    size_t valueEnd = html.find('"', valueStart);
    if (valueEnd == std::string::npos) {
      result += html.substr(stylePos);
      break;
    }

    std::string styleValue = DecodeHTMLEntities(html.substr(valueStart, valueEnd - valueStart));
    std::string styleObj =
        useDoubleQuotes ? StyleStringToJSXObject(styleValue) : StyleStringToVueObject(styleValue);

    if (useDoubleQuotes) {
      result += "style={" + styleObj + "}";
    } else {
      result += ":style=\"" + styleObj + "\"";
    }

    pos = valueEnd + 1;
  }

  return result;
}

static std::string ReplaceClassWithClassName(const std::string& html) {
  std::string result;
  result.reserve(html.size());
  size_t pos = 0;

  while (pos < html.size()) {
    size_t classPos = html.find("class=\"", pos);
    if (classPos == std::string::npos) {
      result += html.substr(pos);
      break;
    }

    if (classPos > 0) {
      char prevChar = html[classPos - 1];
      if (prevChar != ' ' && prevChar != '\n' && prevChar != '\t' && prevChar != '<') {
        result += html.substr(pos, classPos - pos + 6);
        pos = classPos + 6;
        continue;
      }
    }

    result += html.substr(pos, classPos - pos);
    result += "className=\"";
    pos = classPos + 7;
  }

  return result;
}

static std::string ReplaceHTMLComments(const std::string& html) {
  std::string result;
  result.reserve(html.size());
  size_t pos = 0;

  while (pos < html.size()) {
    size_t commentStart = html.find("<!--", pos);
    if (commentStart == std::string::npos) {
      result += html.substr(pos);
      break;
    }

    result += html.substr(pos, commentStart - pos);

    size_t commentEnd = html.find("-->", commentStart);
    if (commentEnd == std::string::npos) {
      result += html.substr(commentStart);
      break;
    }

    std::string commentContent = html.substr(commentStart + 4, commentEnd - commentStart - 4);
    result += "{/*" + commentContent + "*/}";
    pos = commentEnd + 3;
  }

  return result;
}

// Rewrites kebab-case SVG attribute names to the camelCase form React/JSX expects. React
// treats DOM attributes as React props and only recognises the camelCase spelling for SVG
// presentation attributes (stop-color → stopColor etc.); kebab-case would be dropped with a
// "Invalid DOM property" warning and the visual (dashes, gradient stops, stroke widths...)
// would disappear. The transform preserves data-* and aria-* attributes, which React allows
// in their dashed form.
static std::string TransformSVGAttributeNames(const std::string& jsx) {
  static const std::unordered_map<std::string, std::string> kKebabToCamel = {
      {"clip-path", "clipPath"},
      {"clip-rule", "clipRule"},
      {"color-interpolation-filters", "colorInterpolationFilters"},
      {"fill-opacity", "fillOpacity"},
      {"fill-rule", "fillRule"},
      {"flood-color", "floodColor"},
      {"flood-opacity", "floodOpacity"},
      {"stop-color", "stopColor"},
      {"stop-opacity", "stopOpacity"},
      {"stroke-dasharray", "strokeDasharray"},
      {"stroke-dashoffset", "strokeDashoffset"},
      {"stroke-linecap", "strokeLinecap"},
      {"stroke-linejoin", "strokeLinejoin"},
      {"stroke-miterlimit", "strokeMiterlimit"},
      {"stroke-opacity", "strokeOpacity"},
      {"stroke-width", "strokeWidth"},
      {"text-anchor", "textAnchor"},
      {"dominant-baseline", "dominantBaseline"},
      {"alignment-baseline", "alignmentBaseline"},
      {"gradient-units", "gradientUnits"},
      {"gradient-transform", "gradientTransform"},
      {"pattern-units", "patternUnits"},
      {"pattern-content-units", "patternContentUnits"},
      {"pattern-transform", "patternTransform"},
  };

  std::string result;
  result.reserve(jsx.size());
  size_t pos = 0;
  while (pos < jsx.size()) {
    char c = jsx[pos];
    if (c != '<') {
      result += c;
      pos++;
      continue;
    }

    size_t tagClose = jsx.find('>', pos);
    if (tagClose == std::string::npos) {
      result += jsx.substr(pos);
      break;
    }

    // Skip comment/CDATA/processing-instruction tokens that just happen to start with '<'.
    if (pos + 1 < jsx.size() && (jsx[pos + 1] == '!' || jsx[pos + 1] == '?')) {
      result += jsx.substr(pos, tagClose - pos + 1);
      pos = tagClose + 1;
      continue;
    }

    std::string tagContent = jsx.substr(pos, tagClose - pos + 1);
    std::string rewritten;
    rewritten.reserve(tagContent.size());
    size_t i = 0;
    while (i < tagContent.size()) {
      char ch = tagContent[i];
      // Attribute names begin after whitespace and are terminated by '=' or whitespace or '>'.
      // Names always start with a letter; look for kebab identifiers and rewrite if matched.
      if ((ch == ' ' || ch == '\t' || ch == '\n') && i + 1 < tagContent.size() &&
          (std::isalpha(static_cast<unsigned char>(tagContent[i + 1])) ||
           tagContent[i + 1] == '-')) {
        rewritten += ch;
        i++;
        size_t nameStart = i;
        while (i < tagContent.size()) {
          char nc = tagContent[i];
          if (std::isalnum(static_cast<unsigned char>(nc)) || nc == '-' || nc == '_' || nc == ':') {
            i++;
          } else {
            break;
          }
        }
        std::string name = tagContent.substr(nameStart, i - nameStart);
        auto it = kKebabToCamel.find(name);
        if (it != kKebabToCamel.end()) {
          rewritten += it->second;
        } else {
          rewritten += name;
        }
        continue;
      }
      rewritten += ch;
      i++;
    }

    result += rewritten;
    pos = tagClose + 1;
  }
  return result;
}

static std::string SerializeToReactJSX(const std::string& nativeHTML,
                                       const HTMLExportOptions& options) {
  std::string jsx = nativeHTML;

  jsx = ReplaceClassWithClassName(jsx);
  jsx = ReplaceHTMLComments(jsx);
  jsx = TransformSVGAttributeNames(jsx);
  jsx = TransformStyleAttributes(jsx, true);

  std::string indentStr(static_cast<size_t>(options.indent * 2), ' ');
  std::string singleIndent(static_cast<size_t>(options.indent), ' ');

  std::string result;
  result.reserve(jsx.size() + 400);
  result += kGeneratedCommentJS;
  result += "export default function " + options.componentName + "() {\n";
  result += singleIndent + "return (\n";

  size_t lineStart = 0;
  while (lineStart < jsx.size()) {
    size_t lineEnd = jsx.find('\n', lineStart);
    if (lineEnd == std::string::npos) {
      lineEnd = jsx.size();
    }

    std::string line = jsx.substr(lineStart, lineEnd - lineStart);
    result += indentStr + line + "\n";
    lineStart = lineEnd + 1;
  }

  result += singleIndent + ");\n";
  result += "}\n";

  return result;
}

static std::string SerializeToVueSFC(const std::string& nativeHTML,
                                     const HTMLExportOptions& options) {
  std::string vue = TransformStyleAttributes(nativeHTML, false);

  std::string singleIndent(static_cast<size_t>(options.indent), ' ');

  std::string result;
  result.reserve(vue.size() + 400);
  result += "<!-- Generated by PAGX HTMLExporter. Do not edit. -->\n";
  result += "<template>\n";

  size_t lineStart = 0;
  while (lineStart < vue.size()) {
    size_t lineEnd = vue.find('\n', lineStart);
    if (lineEnd == std::string::npos) {
      lineEnd = vue.size();
    }

    std::string line = vue.substr(lineStart, lineEnd - lineStart);
    result += singleIndent + line + "\n";
    lineStart = lineEnd + 1;
  }

  result += "</template>\n\n";
  result += "<script setup>\n";
  result += "// No setup logic required for this component.\n";
  result += "</script>\n";

  return result;
}

